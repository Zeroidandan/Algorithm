// 显示100到1000之间所有既能被5整除，也能被6整除的数组，要求每行显示10个数字
// 行字符串
var rowstr = '';
// 统计每行的数字有多少个
var count = 0;

for (var i = 100; i <= 1000; i++) {
    if (i % 5 == 0 && i % 6 == 0) {
        // 找到数字拼接字符串
        rowstr += i + ' ';
        // 计数器加1
        count++;

        if (count == 10) {
            console.log(rowstr);
            // 每行10个数字后，计数器清零
            count = 0;
            // 每行10个数字后，字符串也要清零
            rowstr = '';
        }
    }
}